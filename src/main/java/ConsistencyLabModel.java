public class ConsistencyLabModel {
    private String username;
    private String numCarts;
    private String output;

    public ConsistencyLabModel() {
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(final String username) {
        this.username = username;
    }

    public String getNumCarts() {
        return numCarts;
    }

    public void setNumCarts(final String umCarts) {
        this.numCarts = umCarts;
    }

    public String getOutput() {
        return output;
    }

    public void setOutput(final String output) {
        this.output = output;
    }
}